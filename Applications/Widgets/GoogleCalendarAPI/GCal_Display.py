    def getGCalToday(self):
        SCOPES = ['https://www.googleapis.com/auth/calendar', 'https://www.googleapis.com/auth/calendar.readonly', 'https://www.googleapis.com/auth/calendar.events', 'https://www.googleapis.com/auth/calendar.events.readonly']

        # Creates and nullifies the variable "creds"
        creds = None
        # If the auth token exists, the "creds" variable is loaded with the token and it's scope
        if os.path.exists('token.json'):
            creds = Credentials.from_authorized_user_file('token.json', SCOPES)
        # Checks if the auth token does not exist or is not valid 
        if not creds or not creds.valid:
            # Attempts to refresh the auth token if it exists but has expired
            if creds and creds.expired and creds.refresh_token:
                creds.refresh(Request()) ## Problem with the Google Code or the "Credentials" Function at this time. Does not auto refresh the token!
            # Creates a new auth token using the manually made "credentials.json" that was downloaded from the Google Cloud Console 
            else:
                flow = InstalledAppFlow.from_client_secrets_file('credentials.json', SCOPES)
                creds = flow.run_local_server(port = 0)
            with open('token.json', 'w') as token:
                token.write(creds.to_json())

        # Uses the GoogleApplication Build function to define the parameters for the Calendar

       # service = build('calendar', 'v3', credentials=creds)
        # Defining what our current date and setting our variables for the time ranges we want to capture
        get_today = datetime.datetime.now().isoformat() + 'Z'
        today = get_today.split("T")
    
        get_morning_start =  [today[0], "03:00:00.000000Z"]
        morning_range_start = "T".join(get_morning_start)
        get_morning_end = [today[0], "10:59:59.000000Z"]
        morning_range_end = "T".join(get_morning_end)

        get_afternoon_start =  [today[0], "11:30:00.000000Z"]
        afternoon_range_start = "T".join(get_afternoon_start)
        get_afternoon_end = [today[0], "16:59:59.000000Z"]
        afternoon_range_end = "T".join(get_afternoon_end)

        get_evening_start =  [today[0], "17:30:00.000000Z"]
        evening_range_start = "T".join(get_evening_start)
        get_evening_end = [today[0], "23:59:59.000000Z"]
        evening_range_end = "T".join(get_evening_end)

        # Setting the parameters for the date ranges used against the Calendar
        #self.events = service

        self.morning_events = build('calendar', 'v3', credentials=creds).events().list(calendarId='primary', orderBy='startTime', timeMin=morning_range_start, timeMax=morning_range_end, singleEvents=True).execute()
        self.afternoon_events = build('calendar', 'v3', credentials=creds).events().list(calendarId='primary', timeMin=afternoon_range_start, timeMax=afternoon_range_end, singleEvents=True, orderBy='startTime').execute()
        self.evening_events = build('calendar', 'v3', credentials=creds).events().list(calendarId='primary', timeMin=evening_range_start, timeMax=evening_range_end, singleEvents=True, orderBy='startTime').execute()

