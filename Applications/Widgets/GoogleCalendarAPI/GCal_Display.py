    def getDate(self):
        curr_month_day = []
        curr_month_day.append(datetime.datetime.now().strftime('%B'))
        curr_month_day.append(datetime.datetime.now().day)
        set_month_day = "{month} {day}".format(month = curr_month_day[0], day = curr_month_day[1])
        return set_month_day

    def morningTimes(self):

        self.start_times = []

        for event in self.morning_events['items']:
            get_start_time = event['start'].get('dateTime')
            get_hrs = int(get_start_time[11:13])
            get_mins = get_start_time[14:16]
            combine_times = [str(get_hrs), get_mins]
            new_time = ":".join(combine_times)
            grab_start_time = "%s am"%new_time
            self.start_times.append(grab_start_time)

        
        if self.start_times != []:
            if len(self.start_times) == 1:
                return self.start_times[0]
            elif len(self.start_times) == 2:
                morning_events_times = "\n\n".join(self.start_times)
                return morning_events_times

